public class Email {

	//主键id
	private Integer id;
	
	//邮箱地址
	private String emailAddress;
	
	//邮箱类型后缀：163.com,126.com
	private String emailType;
	
	//邮箱数据是否有效：0：无效，1：有效
	private Integer dataIsValid;
	
	//邮箱是否订阅：0：否，1：是
	private Integer emailIsSubscibe;
	
	//省份
	private String province;
	
	//城市
	private String city;
	
	//邮箱来源
	private String source;
	
	//邮箱状态：0：无效,1：有效
	private Integer emailState;
	
	//数据创建时间
	private Timestamp createDate;
	
	//数据更新时间
	private Timestamp updateDate;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getEmailAddress() {
		return emailAddress;
	}

	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	public String getEmailType() {
		return emailType;
	}

	public void setEmailType(String emailType) {
		this.emailType = emailType;
	}

	public Integer getDataIsValid() {
		return dataIsValid;
	}

	public void setDataIsValid(Integer dataIsValid) {
		this.dataIsValid = dataIsValid;
	}

	public Integer getEmailIsSubscibe() {
		return emailIsSubscibe;
	}

	public void setEmailIsSubscibe(Integer emailIsSubscibe) {
		this.emailIsSubscibe = emailIsSubscibe;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public Integer getEmailState() {
		return emailState;
	}

	public void setEmailState(Integer emailState) {
		this.emailState = emailState;
	}

	public Timestamp getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Timestamp createDate) {
		this.createDate = createDate;
	}

	public Timestamp getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Timestamp updateDate) {
		this.updateDate = updateDate;
	}

}
