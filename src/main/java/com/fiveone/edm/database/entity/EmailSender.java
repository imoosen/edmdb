public class EmailSender {
	
	//主键id
	private int id;
	
	//发件人邮箱地址
	private String emailSenderAddress;
	
	//发件人密码
	private String password;

	//邮箱发件人状态：0：无效,1：有效
	private Integer emailSenderState;
	
	//数据创建时间
	private Timestamp createDate;

	//数据修改时间
	private  Timestamp updateDate;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEmailSenderAddress() {
		return emailSenderAddress;
	}

	public void setEmailSenderAddress(String emailSenderAddress) {
		this.emailSenderAddress = emailSenderAddress;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public Integer getEmailSenderState() {
		return emailSenderState;
	}

	public void setEmailSenderState(Integer emailSenderState) {
		this.emailSenderState = emailSenderState;
	}

	public Timestamp getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Timestamp createDate) {
		this.createDate = createDate;
	}

	public Timestamp getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Timestamp updateDate) {
		this.updateDate = updateDate;
	}
	
}
